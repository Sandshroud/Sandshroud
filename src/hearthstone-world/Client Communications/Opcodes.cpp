uint32 OpcodeManager::ConvertOpcodeForInput(uint32 opcode)
{
    if(inputMap.find(opcode) == inputMap.end())
        return MSG_NULL_ACTION;
    return inputMap.at(opcode);
}

uint32 OpcodeManager::ConvertOpcodeForOutput(uint32 opcode)
{
    if(outputMap.find(opcode) == outputMap.end())
        return MSG_NULL_ACTION;
    return outputMap.at(opcode);
}

const char* OpcodeManager::GetOpcodeName(uint32 opcode)
{
    if(opcodeNames.find(opcode) == opcodeNames.end())
        return "UNKNOWN";
    return opcodeNames.at(opcode);
}

