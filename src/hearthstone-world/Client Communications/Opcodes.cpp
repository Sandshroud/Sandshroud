void OpcodeManager::SetOpcodeData(uint32 opcode, char* name, uint32 newOpcode)
{
    if(inputMap.find(newOpcode) != inputMap.end() || outputMap.find(opcode) != outputMap.end())
    {
        sLog.Error("OpcodeMgr", "Initializing opcodes, %s using previously set opcode data 0x%.3X 0x%.4X", name, opcode, newOpcode);
        Sleep(5000);
        exit(0);
        return;
    }

    opcodeNames.insert(std::make_pair(opcode, name));
    outputMap.insert(std::make_pair(opcode, newOpcode));
    inputMap.insert(std::make_pair(newOpcode, opcode));
}
